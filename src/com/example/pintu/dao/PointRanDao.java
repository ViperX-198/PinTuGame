package com.example.pintu.dao;

public class PointRanDao {
private String name;
private String diff;
private int score;
private int paiming;
private int id;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getDiff() {
	return diff;
}
public void setDiff(String diff) {
	this.diff = diff;
}
public int getScore() {
	return score;
}
public void setScore(int score) {
	this.score = score;
}
public int getPaiming() {
	return paiming;
}
public void setPaiming(int paiming) {
	this.paiming = paiming;
}

}
